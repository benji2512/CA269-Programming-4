public class FiveToTwenty{
    public static void main(String[]args){
        int i = 5;

        while (i <= 20){
            System.out.print(i + " ");
            i++;
        }
        System.out.println();

        for(int j=5;j<=20;j++)
            System.out.print(j + " ");

        System.out.println();
    }
}
