public class TenToOne{
    public static void main(String[]args){
        int i = 10;

        while (i >= 1){
            System.out.print(i + " ");
            i--;
        }
        System.out.println();

        for(int j=10;j>=1;j--)
            System.out.print(j + " ");

        System.out.println();
    }
}
